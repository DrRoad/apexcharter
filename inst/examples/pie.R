# Packages ----------------------------------------------------------------

library(apexcharter)





# Simple pie --------------------------------------------------------------

apexchart() %>% 
  ax_chart(type = "pie") %>% 
  ax_series(23, 45, 56)




# Donut -------------------------------------------------------------------


apexchart() %>% 
  ax_chart(type = "donut") %>% 
  ax_series2(l = c(23, 45, 56))


